# CP-2: archive boundary — finalize writes ONLY the three allowed files
# ---------------------------------------------------------------------------

def test_finalize_only_writes_allowed_files(capsys: pytest.CaptureFixture, gov_pass: Path) -> None:
    """finalize must not write files outside feature.yaml, feature-index.yaml, and summary.md."""
    def _all_files(root: Path) -> set[str]:
        return {str(p.relative_to(root)) for p in root.rglob("*") if p.is_file()}

    before = _all_files(gov_pass)
    _run(
        ["finalize", "--governance-repo", str(gov_pass), "--feature-id", "lens-dev-test-feature", "--confirm"],
        capsys,
    )
    after = _all_files(gov_pass)
    new_files = after - before
    unexpected = {f for f in new_files if Path(f).name not in ALLOWED_WRITE_FILES}
    assert not unexpected, f"finalize wrote unexpected files: {unexpected}"


