# ---------------------------------------------------------------------------
# CP-1 / CP-5: finalize --confirm → archives feature, writes exactly 3 files
# ---------------------------------------------------------------------------

def test_finalize_archives_feature(capsys: pytest.CaptureFixture, gov_pass: Path) -> None:
    """finalize --confirm sets phase=complete, archives the index, and writes summary.md."""
    exit_code, result = _run(
        ["finalize", "--governance-repo", str(gov_pass), "--feature-id", "lens-dev-test-feature", "--confirm"],
        capsys,
    )
    assert exit_code == 0, f"Expected exit 0, got {exit_code}. Result: {result}"
    assert result["status"] == "complete"
    assert result["feature_id"] == "lens-dev-test-feature"
    assert "archived_at" in result

    # Verify feature.yaml updated
    fp = gov_pass / "features" / "lens-dev" / "new-codebase" / "lens-dev-test-feature" / "feature.yaml"
    fd = yaml.safe_load(fp.read_text(encoding="utf-8"))
    assert fd["phase"] == "complete"
    assert "completed_at" in fd

    # Verify feature-index.yaml updated
    idx = yaml.safe_load((gov_pass / "feature-index.yaml").read_text(encoding="utf-8"))
    entry = next(e for e in idx["features"] if e["id"] == "lens-dev-test-feature")
    assert entry["status"] == "archived"

    # Verify summary.md written
    summary_path = fp.parent / "summary.md"
    assert summary_path.exists(), "summary.md must be written by finalize"
    assert "archived" in summary_path.read_text(encoding="utf-8").lower()


