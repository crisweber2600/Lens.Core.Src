    after_keys = set(yaml.safe_load(fp.read_text(encoding="utf-8")).keys())
    new_keys = after_keys - before_keys
    allowed_new_keys = {"completed_at", "phase_transitions"}
    unexpected = new_keys - allowed_new_keys
    assert not unexpected, f"finalize added unexpected YAML keys to feature.yaml: {unexpected}"


